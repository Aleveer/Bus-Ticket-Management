INSERT INTO
    `trip` (
        `trip_id`,
        `departure_time`,
        `arrival_time`,
        `price`,
        `status`,
        `bus_id`,
        `driver_id`,
        `controller_id`,
        `staff_id`,
        `route_code`
    )
SELECT
    NULL,
    CASE
        WHEN RAND() < 0.5 THEN DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 30) DAY) -- Random past date within 30 days
        ELSE DATE_ADD(NOW(), INTERVAL FLOOR(RAND() * 30) DAY) -- Random future date within 30 days
    END AS departure_time,
    CASE
        WHEN RAND() < 0.5 THEN DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 30 + 1) DAY) -- Random past date within 31 days
        ELSE DATE_ADD(NOW(), INTERVAL FLOOR(RAND() * 30 + 1) DAY) -- Random future date within 31 days
    END AS arrival_time,
    ROUND(RAND() * 100 + 20, 2) AS price,
    -- Random price between 20 and 120
    CASE
        WHEN RAND() < 0.5 THEN CASE
            WHEN RAND() < 0.5 THEN 'finished'
            ELSE 'arriving'
        END
        ELSE CASE
            WHEN RAND() < 0.7 THEN 'waiting'
            WHEN RAND() < 0.85 THEN 'arriving'
            ELSE 'started'
        END
    END AS status,
    bus.bus_id,
    driver.driver_id,
    controller.controller_id,
    staff.staff_id,
    route.code AS route_code
FROM
    (
        SELECT
            1
        FROM
            DUAL
        LIMIT
            3
    ) AS x
    JOIN (
        SELECT
            bus_id
        FROM
            `bus`
        ORDER BY
            RAND()
        LIMIT
            3
    ) AS bus ON 1 = 1
    JOIN (
        SELECT
            driver_id
        FROM
            `driver`
        ORDER BY
            RAND()
        LIMIT
            3
    ) AS driver ON 1 = 1
    JOIN (
        SELECT
            controller_id
        FROM
            `controller`
        ORDER BY
            RAND()
        LIMIT
            3
    ) AS controller ON 1 = 1
    JOIN (
        SELECT
            staff_id
        FROM
            `staff`
        ORDER BY
            RAND()
        LIMIT
            3
    ) AS staff ON 1 = 1
    JOIN (
        SELECT
            `code`
        FROM
            `route`
        ORDER BY
            RAND()
        LIMIT
            3
    ) AS route ON 1 = 1;